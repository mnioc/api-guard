operator_map = {
    '==': lambda x, y: x == y,
    '!=': lambda x, y: x != y,
    '>': lambda x, y: x > y,
    '<': lambda x, y: x < y,
    '>=': lambda x, y: x >= y,
    '<=': lambda x, y: x <= y,
    'in': lambda x, y: x in y,
    'not in': lambda x, y: x not in y,
    'is': lambda x, y: x is y,
    'is not': lambda x, y: x is not y,
}
